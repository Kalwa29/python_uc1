x = input("Me de um numero inteiro :")
print(f"O valor inicial de x é {x}")

x = int(x) + 5
print(f"O valor da soma do numero inteiro é {x} ")

x=int(x) - 3
print(f"O valor da subtracao do numero inteiro é {x} ")

x=int(x) * 2
print(f"O valor da multiplicacao do numero inteiro é {x} ")

x=int(x) / 4
print(f"O valor da divisao do numero inteiro é {x} ")

x=int(x) // 3
print(f"O valor da divisao_inteira do numero inteiro é {x} ")

x=int(x) % 2
print(f"O valor do modulo do numero inteiro é {x} ")

x=int(x) ** 3
print(f"O valor da exponenciacao do numero inteiro é {x} ")
